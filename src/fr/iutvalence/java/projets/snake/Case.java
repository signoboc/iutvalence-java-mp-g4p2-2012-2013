package fr.iutvalence.java.projets.snake;

/**
 * Enumération de constantes symbolisant les différents éléments du terrain.
 * @author berardk
 */
public enum Case
{
	/**
	 * Désigne une case vide sur le terrain
	 */
	VIDE,
	/**
	 * Désigne un mur sur le terrain
	 */
	MUR,
	/**
	 * Désigne la pastille sur le terrain
	 */
	PASTILLE,
	/**
	 * Désigne la tête du serpent sur le terrain
	 */
	TETE,
	/**
	 * Désigne les éléments du corps du serpent sur le terrain
	 */
	CORPS
}
